import React, { useState, useEffect } from "react";
import {
  Model,
  PeriodUnit,
  SimulationTimeType,
  SimulationObjectType,
  StateListManager,
  ComponentType,
  EnvelopeMessageType,
  EnvelopeBase,
  ValidationState,
} from "@quodsi/shared";
import { Settings, Hash, PlaySquare, FileJson, Info, Users, Wrench, AlertTriangle } from "lucide-react";
import StatesEditor from "./StatesEditor";
import { AccordionSection } from "../shared/AccordionSection";
import ScenarioEditor from "./ScenarioEditor";
import { ResourceRequirementsManager } from "./ResourceRequirementsManager";
import { ResourceRequirementModal } from "./ResourceRequirementModal";
import { convertStructureToRootClauses, convertRootClausesToStructure, TeamStructure } from "../../utils/resourceRequirementConverter";
import { useMessaging } from "../../messaging/MessageProvider";
import { ModelDefinitionViewer } from "../modelPanel/ModelDefinitionViewer";
import { useModelOpsSender } from "../../messaging/senders/modelOpsSender";
import { useElementOpsState } from "../../messaging/hooks/useElementOpsState";
import { useFormSync, useSaveCompletionDetector } from "./hooks/useEditorState";
import {
  extractModelData,
  updateModelImmutably,
  isAutoGeneratedRequirement,
  prepareRequirementsForStorage,
  type ModelInput,
} from "../utils/modelEditorHelpers";
import { ValidationDashboard } from "./ValidationDashboard";

import { EditorReferenceData, ResourceRequirement } from "@quodsi/shared";

// ============================================================================
// TYPES
// ============================================================================

interface Props {
  model: Model;
  onSave: (model: Model) => void;
  onCancel: () => void;
  onRemoveModel?: () => void;
  states: StateListManager;
  onStatesChange: (states: StateListManager) => void;
  referenceData?: EditorReferenceData;
  resourceRequirements?: ResourceRequirement[];
  validationState?: ValidationState | null;
}

type EditorTab = "basic" | "states" | "requirements" | "scenarios" | "utilities" | "validation";

/**
 * Type for tracking resource requirement being edited in modal
 */
interface EditingRequirement {
  id: string;
  name: string;
  structure: TeamStructure;
}

/**
 * Tab navigation configuration for ModelEditor.
 * Defines all available tabs, their icons, titles, and tooltips.
 */
const TAB_CONFIG = [
  {
    id: "basic" as const,
    title: "Basic Settings",
    icon: Settings,
    tooltip: "Configure model name, simulation time settings, and runtime parameters"
  },
  {
    id: "validation" as const,
    title: "Validation",
    icon: AlertTriangle,
    tooltip: "View comprehensive model validation results and resolve any issues"
  },
  {
    id: "states" as const,
    title: "State Definitions",
    icon: Hash,
    tooltip: "Define model-level state variables that can be accessed and modified throughout the simulation"
  },
  {
    id: "requirements" as const,
    title: "Resource Requirements",
    icon: Users,
    tooltip: "Create reusable resource requirement templates that define which resources are needed for activities"
  },
  {
    id: "scenarios" as const,
    title: "Simulation Scenarios",
    icon: PlaySquare,
    tooltip: "Configure and manage simulation scenarios with different parameter sets and run configurations"
  },
  {
    id: "utilities" as const,
    title: "Utilities",
    icon: Wrench,
    tooltip: "Model utilities, diagnostics, and maintenance functions"
  },
];

/**
 * Default random seed value used when no seed is specified.
 * This provides reproducible simulation results for testing and debugging.
 */
const DEFAULT_RANDOM_SEED = 12345;

/**
 * TabHeader - Consistent header for tab content sections
 *
 * Displays an icon, title, and tooltip for each tab's content area.
 * Used across all model editor tabs for visual consistency.
 *
 * @param icon - Lucide icon component to display
 * @param title - Tab section title
 * @param tooltip - Helpful description shown on hover
 */
const TabHeader: React.FC<{ icon: React.ElementType; title: string; tooltip: string }> = ({
  icon: Icon,
  title,
  tooltip,
}) => (
  <div className="flex items-center gap-1 mb-1">
    <Icon className="w-3 h-3 text-blue-500" />
    <span className="text-xs font-medium text-gray-700">{title}</span>
    <span title={tooltip}>
      <Info className="w-3 h-3 text-gray-400 hover:text-gray-600 cursor-help" />
    </span>
  </div>
);

/**
 * ModelEditor - Component for editing model-level simulation settings
 *
 * The ModelEditor orchestrates the configuration of simulation model settings across
 * multiple tabs, including basic properties, state variables, resource requirements,
 * and simulation scenarios. It acts as a container for specialized sub-editors.
 *
 * Features:
 * - Four-tab interface: Basic Settings, State Definitions, Resource Requirements, and Scenarios
 * - Controlled component with immediate UI updates
 * - Manual save for basic fields (name, reps, seed, time settings)
 * - Auto-save for states, requirements, and scenarios (handled by sub-editors)
 * - Model JSON viewer for debugging and inspection
 *
 * Tabs:
 * - Basic: Model name, simulation parameters (reps, seed), and time configuration
 * - States: Model-level state variables accessible throughout the simulation
 * - Requirements: Reusable resource requirement templates for activities
 * - Scenarios: Simulation scenario configuration and management
 *
 * State Management:
 * - Maintains local draft state (localModelDraft) for immediate UI updates
 * - Syncs with Redux for save state tracking (isSaving)
 * - Uses custom hooks for model switching and save completion detection
 * - Listens for MODEL_JSON_RESPONSE messages for the model viewer
 *
 * Save Behavior:
 * - Basic tab: Requires Save button click to persist changes
 * - States tab: Auto-saves immediately (Save/Cancel buttons hidden)
 * - Requirements tab: Auto-saves immediately
 * - Scenarios tab: Auto-saves immediately
 *
 * @param props - Component props
 * @returns Rendered model editor component
 */
const ModelEditor: React.FC<Props> = ({ model, onSave, onCancel, onRemoveModel, states, onStatesChange, referenceData, resourceRequirements, validationState }) => {
  // ============================================================================
  // STATE MANAGEMENT
  // ============================================================================

  const [activeTab, setActiveTab] = useState<EditorTab>("basic");
  const [requirementModalOpen, setRequirementModalOpen] = useState(false);
  const [editingRequirement, setEditingRequirement] = useState<EditingRequirement | null>(null);
  const [isModelViewerOpen, setIsModelViewerOpen] = useState(false);
  const [modelJson, setModelJson] = useState<object | null>(null);
  const [isAdvancedExpanded, setIsAdvancedExpanded] = useState(false); // Start collapsed
  const { selection } = useMessaging();
  const { requestModelJson, updateResourceRequirements } = useModelOpsSender();

  // Listen for MODEL_JSON_RESPONSE
  useEffect(() => {
    const handleMessage = (event: MessageEvent) => {
      const msg = event.data as EnvelopeBase;

      // Check if this is a MODEL_JSON_RESPONSE message
      if (msg?.type === EnvelopeMessageType.MODEL_JSON_RESPONSE) {
        const data = msg.data as {
          success: boolean;
          modelJson?: any;
          error?: string;
        };

        if (data.success && data.modelJson) {
          setModelJson(data.modelJson);
          setIsModelViewerOpen(true);
        } else {
          console.error('[ModelEditor] Failed to get model JSON:', data.error);
        }
      }
    };

    window.addEventListener('message', handleMessage);
    return () => window.removeEventListener('message', handleMessage);
  }, []);

  // Direct form state management
  const [localModelDraft, setLocalModelDraft] = useState<Model>(() => extractModelData(model));
  const [hasPendingChanges, setHasPendingChanges] = useState(false);

  // Get element operations state from Redux
  const elementOpsState = useElementOpsState();

  /**
   * Redux-managed state for save operation tracking.
   *
   * isSaving: true when save is in progress (shows loading state)
   *
   * This is managed by Redux elementOpsState to coordinate saves across
   * multiple editor instances.
   */
  const isSaving = localModelDraft.id ? elementOpsState.isSaving(localModelDraft.id) : false;

  // Custom hooks for state synchronization
  useFormSync(
    model.id,
    hasPendingChanges,
    () => extractModelData(model),
    setLocalModelDraft,
    setHasPendingChanges
  );

  useSaveCompletionDetector(isSaving, setHasPendingChanges);

  // Guard against invalid model data
  if (!localModelDraft?.id) {
    return (
      <div className="p-2 bg-red-50 border border-red-200 rounded text-sm">
        <div className="text-red-600 font-medium">Invalid model data</div>
        <div className="text-xs text-red-500 mt-1">Model data missing required properties</div>
      </div>
    );
  }

  // ============================================================================
  // EVENT HANDLERS
  // ============================================================================

  /**
   * Handles changes to form input fields with automatic type conversion.
   *
   * Updates are applied immediately to localModelDraft for responsive UI,
   * and marked as pending (requiring Save button click to persist).
   *
   * Type conversion:
   * - number inputs: Parsed as float with fallback to 0 for invalid values
   * - datetime-local inputs: Converted to Date objects or null
   * - all other inputs: Kept as strings
   *
   * @param e - Input change event
   */
  const handleChange = (e: React.ChangeEvent<HTMLInputElement | HTMLSelectElement>) => {
    const { name, value, type } = e.target;

    // Convert values based on input type
    let convertedValue: string | number | Date | null = value;
    if (type === 'number') {
      const numValue = parseFloat(value);
      convertedValue = isNaN(numValue) ? 0 : numValue;
    } else if (type === 'datetime-local') {
      convertedValue = value ? new Date(value) : null;
    }

    setLocalModelDraft(prev => updateModelImmutably(prev, { [name]: convertedValue } as Partial<Model>));
    setHasPendingChanges(true);
  };

  /**
   * Saves the current model draft state.
   *
   * Constructs a complete Model object with all required properties and defaults,
   * then invokes the parent onSave callback. Redux manages the isSaving state automatically.
   *
   * Note: Does NOT directly modify hasPendingChanges - that's handled by the
   * save completion detector to avoid race conditions.
   */
  const handleSave = () => {
    const modelToSave: Model = {
      ...localModelDraft,
      type: "Model" as any, // Cast needed: SimulationObjectType.Model is enum, type field expects string literal
      // Ensure all properties have defaults
      reps: localModelDraft.reps || 1,
      seed: localModelDraft.seed || DEFAULT_RANDOM_SEED,
      simulationTimeType: localModelDraft.simulationTimeType || SimulationTimeType.Clock,
      oneClockUnit: localModelDraft.oneClockUnit || PeriodUnit.HOURS,
      warmupClockPeriod: localModelDraft.warmupClockPeriod || 0,
      warmupClockPeriodUnit: localModelDraft.warmupClockPeriodUnit || PeriodUnit.HOURS,
      runClockPeriod: localModelDraft.runClockPeriod || 0,
      runClockPeriodUnit: localModelDraft.runClockPeriodUnit || PeriodUnit.HOURS,
      warmupDateTime: localModelDraft.warmupDateTime || null,
      startDateTime: localModelDraft.startDateTime || null,
      finishDateTime: localModelDraft.finishDateTime || null,
    };

    onSave(modelToSave);
    // Note: isSaving state is now managed by Redux through elementOpsState
  };

  /**
   * Cancels editing and closes the editor.
   *
   * Discards all pending changes by:
   * - Re-extracting fresh data from model prop
   * - Clearing hasPendingChanges flag (disables Save button)
   * - Calling onCancel prop to close the editor panel
   *
   * Note: State, requirement, and scenario changes were already auto-saved,
   * so they can't be canceled.
   */
  const handleCancel = () => {
    setLocalModelDraft(extractModelData(model));
    setHasPendingChanges(false);
    onCancel();
  };

  /**
   * Requests the model JSON for debugging/inspection.
   *
   * Sends a request to the extension to generate a complete JSON representation
   * of the model. When received, opens the ModelDefinitionViewer modal.
   */
  const handleViewModelClick = () => {
    // Request model JSON from extension
    requestModelJson(selection.documentContext?.documentId || '');
  };

  // ============================================================================
  // RENDER
  // ============================================================================

  return (
    <div className="flex flex-col bg-white">
      <div className="border-b bg-gray-50">
        <div className="flex">
          {TAB_CONFIG.map((tab) => {
            const Icon = tab.icon;
            return (
              <button
                key={tab.id}
                type="button"
                onClick={() => setActiveTab(tab.id)}
                title={tab.title}
                className={`px-3 py-2 border-b-2 ${
                  activeTab === tab.id
                    ? "border-blue-600 text-blue-600"
                    : "border-transparent text-gray-500 hover:text-gray-700"
                }`}
              >
                <Icon className="w-4 h-4" />
              </button>
            );
          })}
        </div>
      </div>

      {activeTab === "basic" && (
        <>
          <form onSubmit={(e) => { e.preventDefault(); handleSave(); }} className="w-full">
            <div className="space-y-2">
                  <TabHeader
                    icon={Settings}
                    title="Basic Settings"
                    tooltip="Configure model name, simulation time settings, and runtime parameters"
                  />
                  {/* Model Name - Always Visible WITH LABEL */}
                  <div>
                    <label className="block text-xs font-medium text-gray-700 mb-1">
                      Model Name
                    </label>
                    <input
                      type="text"
                      name="name"
                      className="w-full px-2 py-1 text-xs border rounded"
                      value={localModelDraft.name}
                      placeholder="Enter model name"
                      onChange={handleChange}
                    />
                </div>

                {/* Run Time - Conditional: Only in Clock mode */}
                {localModelDraft.simulationTimeType === SimulationTimeType.Clock && (
                  <div>
                    <label className="block text-xs font-medium text-gray-700 mb-1">
                      Run Time
                    </label>
                    <div className="grid grid-cols-2 gap-1">
                      <input
                        type="number"
                        name="runClockPeriod"
                        className="w-full px-2 py-1 text-xs border rounded"
                        value={localModelDraft.runClockPeriod || 0}
                        onChange={handleChange}
                        min="0"
                      />
                      <select
                        name="runClockPeriodUnit"
                        className="w-full px-2 py-1 text-xs border rounded bg-white"
                        value={localModelDraft.runClockPeriodUnit || PeriodUnit.HOURS}
                        onChange={handleChange}
                      >
                        {Object.values(PeriodUnit).map((unit) => (
                          <option key={unit} value={unit}>
                            {unit}
                          </option>
                        ))}
                      </select>
                    </div>
                  </div>
                )}

                {/* Advanced Settings - Accordion */}
                <AccordionSection
                  title="Advanced Settings"
                  isExpanded={isAdvancedExpanded}
                  onToggle={() => setIsAdvancedExpanded(!isAdvancedExpanded)}
                >
                  <div className="space-y-2">
                    {/* Replications */}
                    <div>
                      <label className="block text-xs font-medium text-gray-700 mb-1">
                        Replications
                      </label>
                      <input
                        type="number"
                        name="reps"
                        className="w-full px-2 py-1 text-xs border rounded"
                        value={localModelDraft.reps}
                        onChange={handleChange}
                        min="1"
                      />
                    </div>

                    {/* Time Mode */}
                    <div>
                      <label className="block text-xs font-medium text-gray-700 mb-1">
                        Time Mode
                      </label>
                      <select
                        name="simulationTimeType"
                        className="w-full px-2 py-1 text-xs border rounded bg-white"
                        value={localModelDraft.simulationTimeType}
                        onChange={handleChange}
                      >
                        {Object.values(SimulationTimeType).map((type) => (
                          <option key={type} value={type}>
                            {type}
                          </option>
                        ))}
                      </select>
                    </div>

                    {/* Clock Mode Fields - Conditional */}
                    {localModelDraft.simulationTimeType === SimulationTimeType.Clock && (
                      <>
                        {/* Clock Unit */}
                        <div>
                          <label className="block text-xs font-medium text-gray-700 mb-1">
                            Clock Unit
                          </label>
                          <select
                            name="oneClockUnit"
                            className="w-full px-2 py-1 text-xs border rounded bg-white"
                            value={localModelDraft.oneClockUnit}
                            onChange={handleChange}
                          >
                            {Object.values(PeriodUnit).map((unit) => (
                              <option key={unit} value={unit}>
                                {unit}
                              </option>
                            ))}
                          </select>
                        </div>

                        {/* Warmup Time - Simple number + dropdown */}
                        <div>
                          <label className="block text-xs font-medium text-gray-700 mb-1">
                            Warmup Time
                          </label>
                          <div className="grid grid-cols-2 gap-1">
                            <input
                              type="number"
                              name="warmupClockPeriod"
                              className="w-full px-2 py-1 text-xs border rounded"
                              value={localModelDraft.warmupClockPeriod || 0}
                              onChange={handleChange}
                              min="0"
                            />
                            <select
                              name="warmupClockPeriodUnit"
                              className="w-full px-2 py-1 text-xs border rounded bg-white"
                              value={localModelDraft.warmupClockPeriodUnit || PeriodUnit.HOURS}
                              onChange={handleChange}
                            >
                              {Object.values(PeriodUnit).map((unit) => (
                                <option key={unit} value={unit}>
                                  {unit}
                                </option>
                              ))}
                            </select>
                          </div>
                        </div>
                      </>
                    )}

                    {/* Calendar Date Mode Fields - Conditional */}
                    {localModelDraft.simulationTimeType === SimulationTimeType.CalendarDate && (
                      <>
                        <div>
                          <label className="block text-xs font-medium text-gray-700 mb-1">
                            Start Date
                          </label>
                          <input
                            type="datetime-local"
                            name="startDateTime"
                            className="w-full px-2 py-1 text-xs border rounded"
                            value={localModelDraft.startDateTime?.toISOString().slice(0, 16) || ""}
                            onChange={handleChange}
                          />
                        </div>
                        <div>
                          <label className="block text-xs font-medium text-gray-700 mb-1">
                            Finish Date
                          </label>
                          <input
                            type="datetime-local"
                            name="finishDateTime"
                            className="w-full px-2 py-1 text-xs border rounded"
                            value={localModelDraft.finishDateTime?.toISOString().slice(0, 16) || ""}
                            onChange={handleChange}
                          />
                        </div>
                        <div>
                          <label className="block text-xs font-medium text-gray-700 mb-1">
                            Warmup Date
                          </label>
                          <input
                            type="datetime-local"
                            name="warmupDateTime"
                            className="w-full px-2 py-1 text-xs border rounded"
                            value={localModelDraft.warmupDateTime?.toISOString().slice(0, 16) || ""}
                            onChange={handleChange}
                          />
                        </div>
                      </>
                    )}
                  </div>
                </AccordionSection>
              </div>

              {/* Save/Cancel Buttons */}
              <div className="flex justify-end gap-2 pt-2 border-t">
                <button
                  type="button"
                  onClick={handleCancel}
                  disabled={isSaving}
                  className={`px-3 py-1.5 text-xs border rounded ${
                    isSaving ? "opacity-50 cursor-not-allowed" : "hover:bg-gray-50"
                  }`}
                >
                  Cancel
                </button>
                <button
                  type="submit"
                  disabled={!hasPendingChanges || isSaving}
                  className={`px-3 py-1.5 text-xs rounded ${
                    hasPendingChanges && !isSaving
                      ? "bg-blue-600 text-white hover:bg-blue-700"
                      : "bg-gray-300 text-gray-500 cursor-not-allowed"
                  }`}
                >
                  {isSaving ? "Saving..." : "Save"}
                </button>
              </div>
            </form>
        </>
      )}
      {activeTab === "states" && (
        <div>
          <TabHeader
            icon={Hash}
            title="State Definitions"
            tooltip="Define model-level state variables that can be accessed and modified throughout the simulation"
          />
          <StatesEditor
            states={states}
            onStatesChange={onStatesChange}
            defaultComponentType={ComponentType.MODEL}
          />
        </div>
      )}
      {activeTab === "requirements" && (
        <div>
          <TabHeader
            icon={Users}
            title="Resource Requirements"
            tooltip="Create reusable resource requirement templates that define which resources are needed for activities"
          />
          <ResourceRequirementsManager
            requirements={resourceRequirements || []}
            availableResources={referenceData?.resources || []}
            onAdd={() => {
              setEditingRequirement(null);
              setRequirementModalOpen(true);
            }}
            onEdit={(req) => {
              const structure = convertRootClausesToStructure(req.rootClauses);
              setEditingRequirement({ id: req.id, name: req.name, structure });
              setRequirementModalOpen(true);
            }}
            onDelete={(id) => {
              const remainingRequirements = (resourceRequirements || []).filter(req => req.id !== id);
              const requirementsPayload = prepareRequirementsForStorage(
                remainingRequirements,
                referenceData?.resources || []
              );
              updateResourceRequirements(requirementsPayload);
            }}
            getUsageCount={(id) => {
              // Calculate how many operation steps use this requirement
              const activities = referenceData?.activities || [];
              let count = 0;
              for (const activity of activities) {
                const reqIds = activity.operationStepRequirementIds || [];
                count += reqIds.filter(reqId => reqId === id).length;
              }
              return count;
            }}
          />
        </div>
      )}
      {activeTab === "scenarios" && (
        <div>
          <TabHeader
            icon={PlaySquare}
            title="Simulation Scenarios"
            tooltip="Configure and manage simulation scenarios with different parameter sets and run configurations"
          />
          <ScenarioEditor
            documentId={selection.documentContext?.documentId}
          />
        </div>
      )}
      {activeTab === "utilities" && (
        <div className="space-y-3">
          <TabHeader
            icon={Wrench}
            title="Model Utilities"
            tooltip="Model utilities, diagnostics, and maintenance functions"
          />

          {/* Diagnostics Section */}
          <div className="border border-gray-200 rounded-lg p-2">
            <h3 className="text-xs font-semibold text-gray-700 mb-2">Diagnostics</h3>
            <button
              type="button"
              className="w-full text-xs px-2 py-1.5 bg-purple-50 hover:bg-purple-100 text-purple-600 rounded transition-colors flex items-center justify-center gap-1"
              onClick={handleViewModelClick}
              title="View Model Definition as JSON"
            >
              <FileJson className="w-3 h-3" />
              View Model JSON
            </button>
          </div>

          {/* Danger Zone */}
          <div className="border-2 border-red-200 rounded-lg p-2 bg-red-50">
            <div className="flex items-center gap-1 mb-2">
              <AlertTriangle className="w-4 h-4 text-red-600" />
              <h3 className="text-xs font-semibold text-red-700">Danger Zone</h3>
            </div>
            <div className="bg-white border border-red-200 rounded p-2 mb-2">
              <h4 className="text-xs font-semibold text-gray-900 mb-1">Remove Quodsi Data</h4>
              <p className="text-xs text-gray-600 mb-2">
                This will permanently remove all Quodsi simulation data from this document,
                including all activities, resources, entities, generators, connectors, scenarios,
                and configuration. The document will be restored to its original state.
              </p>
              <p className="text-xs font-semibold text-red-600 mb-2">
                ⚠️ This action cannot be undone.
              </p>
              <button
                type="button"
                className="w-full text-xs px-2 py-1.5 bg-red-600 hover:bg-red-700 text-white rounded transition-colors font-medium"
                onClick={onRemoveModel}
                title="Remove all Quodsi data from this document"
              >
                Remove All Quodsi Data
              </button>
            </div>
          </div>
        </div>
      )}
      {activeTab === "validation" && (
        <ValidationDashboard
          validationState={validationState || null}
          referenceData={referenceData}
        />
      )}

      {/* Resource Requirement Modal */}
      <ResourceRequirementModal
        isOpen={requirementModalOpen}
        onClose={() => {
          setRequirementModalOpen(false);
          setEditingRequirement(null);
        }}
        onSave={(data) => {
          const rootClauses = convertStructureToRootClauses(data.structure);

          // Create the updated/new requirement
          let savedRequirement: ResourceRequirement;
          let updatedRequirementsList: ResourceRequirement[];

          if (editingRequirement) {
            // Update existing requirement (could be editing auto or custom)
            savedRequirement = new ResourceRequirement(editingRequirement.id, data.name, rootClauses);
            updatedRequirementsList = (resourceRequirements || []).map(req =>
              req.id === editingRequirement.id ? savedRequirement : req
            );
          } else {
            // Create new requirement (always custom)
            savedRequirement = new ResourceRequirement(`req-${Date.now()}`, data.name, rootClauses);
            updatedRequirementsList = [...(resourceRequirements || []), savedRequirement];
          }

          // IMPORTANT: Only send custom requirements to storage (filter out unmodified auto-requirements)
          // - New requirements (ID starts with "req-") are always custom
          // - Edited requirements (even if ID matches resource) become custom overrides
          // - Unmodified auto-requirements (ID matches resource, wasn't just edited) are filtered out
          const customRequirementsToSave = updatedRequirementsList.filter(req =>
            !isAutoGeneratedRequirement(req, referenceData?.resources || []) || req.id === savedRequirement.id
          );

          // Serialize for storage
          const savePayload = customRequirementsToSave.map(req => ({
            id: req.id,
            name: req.name,
            type: SimulationObjectType.ResourceRequirement,
            rootClauses: req.rootClauses
          }));

          // Send custom-only array to extension
          updateResourceRequirements(savePayload);

          // Close modal
          setRequirementModalOpen(false);
          setEditingRequirement(null);
        }}
        editingRequirement={editingRequirement}
        availableResources={referenceData?.resources || []}
      />

      {/* Model Definition Viewer Modal */}
      {isModelViewerOpen && modelJson && (
        <ModelDefinitionViewer
          modelJson={modelJson}
          onClose={() => setIsModelViewerOpen(false)}
        />
      )}
    </div>
  );
};

export default React.memo(ModelEditor);
